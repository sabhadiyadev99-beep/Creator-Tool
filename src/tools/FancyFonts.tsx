function FontItem({ title, text }: { title: string, text: string }) {
  const [copied, setCopied] = useState(false);

  const handleCopy = () => {
    navigator.clipboard.writeText(text);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div className="p-4 bg-slate-800 rounded-lg group relative border border-slate-700 hover:border-brand transition-all hover:bg-slate-750">
      <div className="text-[9px] uppercase font-bold text-slate-500 mb-1 tracking-widest">{title}</div>
      <div className="text-slate-100 text-lg sm:text-xl font-medium leading-relaxed pr-12 break-all">{text}</div>
      <button 
        onClick={handleCopy}
        className="absolute top-1/2 -translate-y-1/2 right-4 p-2.5 bg-slate-700 hover:bg-brand text-white rounded-lg transition-all opacity-0 group-hover:opacity-100 shadow-lg"
        title="Copy this style"
      >
        {copied ? <Check size={16} className="text-green-400" /> : <Copy size={16} />}
      </button>
    </div>
  );
}

export default function FancyFonts({ tool }: { tool: ToolMetadata }) {
  const [results, setResults] = useState<{ name: string, text: string }[]>([]);

  const generateFonts = (input: string) => {
    if (!input) {
      setResults([]);
      return;
    }
    
    // Core Unicode variations
    const variations = FONT_MAPS.map(style => {
      const transformed = input.split('').map(c => (style.map as any)[c] || (style.map as any)[c.toLowerCase()] || c).join('');
      return {
        name: style.name,
        text: transformed
      };
    });

    // Add some "decorated" styles like LingoJam
    const decorations = [
      { name: 'Arrows', prefix: '➼ ', suffix: ' ➼' },
      { name: 'Sparkles', prefix: '✨ ', suffix: ' ✨' },
      { name: 'Heart', prefix: '❤️ ', suffix: ' ❤️' },
      { name: 'Cool Decor', prefix: '【', suffix: '】' },
      { name: 'Star', prefix: '★彡 ', suffix: ' 彡★' },
    ];

    decorations.forEach(dec => {
      variations.push({
        name: dec.name,
        text: dec.prefix + input + dec.suffix
      });
    });

    setResults(variations);
  };

  return (
    <ToolShell
      title={tool.name}
      description={tool.description}
      inputPlaceholder="Type your text here to see cool styles..."
      onGenerate={generateFonts}
      buttonText="none" // Enable real-time updates
      customOutput={
        results.length > 0 ? (
          <div className="space-y-3">
            {results.map((res, i) => (
              <FontItem key={i} title={res.name} text={res.text} />
            ))}
            <div className="p-8 text-center opacity-30 text-xs italic">
              Keep typing for more variations...
            </div>
          </div>
        ) : null
      }
    />
  );
}
